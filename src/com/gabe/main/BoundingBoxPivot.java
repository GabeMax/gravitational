public class BoundingBoxPivot {
    
    public int xOffset;
    public int yOffset;
    public int width;
    public int height;
    
    public BoundingBoxPivot(int xOffset, int yOffset, int width, int height) {
        this.xOffset = xOffset;
        this.yOffset = yOffset;
        this.width = width;
        this.height = height;
    }
}
